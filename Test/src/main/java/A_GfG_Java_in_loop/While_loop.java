package A_GfG_Java_in_loop;

public class While_loop {

	public static void main(String[] args) {
		int x = 10;
		// it is used for bring x value upto parameter passed
		while (x <= 14) {

			System.out.println("am value of x" + x);
//			here printed value of x
//			then we are using increment operator
			x = x + 1;

		}

	}

}
